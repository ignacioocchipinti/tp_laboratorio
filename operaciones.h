// PROTOTIPO SUMAR()

/** \brief calcula la suma de dos enteros
 *
 * \param a int primer valor a sumar
 * \param b int segundo valor a sumar
 * \return int la suma de a y b
 *
 */
int sumar(int a, int b);



// PROTOTIPO RESTAR()

/** \brief calcula la resta de dos enteros
 *
 * \param a int primer valor a restar
 * \param b int segundo valor a restar
 * \return int la resta de a y b
 *
 */
int restar(int a, int b);



// PROTOTIPO DIVIDIR()

/** \brief calcula la division de dos enteros
 *
 * \param a int primer valor a dividir
 * \param b int segundo valor a dividir
 * \return float resultado de la division de a y b
 *
 */
float dividir(int a, int b);



// PROTOTIPO MULTIPLICAR()

/** \brief calcula la multiplicacion de dos enteros
 *
 * \param a int primer valor a multiplicar
 * \param b int segundo valor a multiplicar
 * \return int resultado de la multiplicacion de a y b
 *
 */
int multiplicar(int a, int b);



// PROTOTIPO FACTORIZARA()

/** \brief calcula el factorial del 1er operando
 *
 * \param a int valor a factorizar
 * \return int el factorial del 1er operando
 *
 */
int factorizarA(int a);



// PROTOTIPO FACTORIZARB()

/** \brief calcula el factorial del 2do operando
 *
 * \param a int valor a factorizar
 * \return int el factorial del 2do operando
 *
 */
int factorizarB(int b);







